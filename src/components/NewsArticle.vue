    <section class="content">
      <p>While much of Tuesday’s elections focused on Congress and several states’ gubernatorial races, voters also weighed in on another contentious issue — marijuana.</p>
      <p>Michigan voters on Tuesday made the state the 10th in the U.S. — and the first in the Midwest — to legalize recreational marijuana. Voters in a second Midwest state, North Dakota, struck down a similar measure that would have legalized recreational use and expunged criminal records for prior marijuana offenders.</p>
      <p>Meanwhile, Missouri and Utah passed initiatives to legalize medical marijuana, joining 31 other states, Washington DC, Guam and Puerto Rico, that already permit medicinal use. Recreational marijuana is already legal in Washington, DC, and nine other states:  Washington, Oregon, Nevada, California, Colorado, Alaska, Vermont, Maine and Massachusetts.</p
      ><p>Here’s what to expect in the new laws:</p>
      <h2>Michigan</h2>
      <p>Michigan’s Proposal 1 permits use for adults 21 and older, yet allows municipalities to ban or restrict marijuana use and commercial enterprise within their boundaries. The new law will allow individuals to grow up to 12 cannabis plants at a time. Public consumption remains prohibited. Michigan has provided for one of the lowest cannabis tax levels in the nation with retailers to pay a 10% excise tax, and consumers picking up the state’s 6% sales tax.</p>
      <p>While 56% of Michigan voted to support legalization, 44% opposed. The bill is expected to go into effect next year with sales beginning in 2020. </p>
      <p>“I am proud of Michigan voters who have overwhelmingly supported this initiative and I am extremely excited about a future where law enforcement and our criminal justice system can focus on real crime and improve public safety,”  Barton Morris Junior, founder of Cannabis Legal Group, told Yahoo Finance.</p>
      <h2>North Dakota</h2>
      <p>North Dakota’s Measure 3 failed to pass. The brake on legalization comes as less of a surprise, as critics warned the bill lacked regulatory and enforcement details. The state’s proposal contained no mandatory tax on marijuana transfers or sales, making revenue projections uncertain. Proponents of the measure viewed it as critical criminal justice reform, as the state has one of the lowest marijuana consumption rates, yet is among the highest for prosecuting marijuana offenses. 
      </p>
      <p class="image-container"><img src="../assets/1.jpg"></p>
      <p>Cole Haymond, an Advisor for Legalize ND, said North Dakota is also home to veterans who could benefit from easier access to the drug. “With a state with a high percentage of veterans it’s disappointing if it doesn’t pass,” Haymod said ahead of the vote. </p>
      <p>Polls leading into midterm elections were mixed on the initiative. An October 11 – 14 poll found 51% of likely voters in favor of legalized recreational use, while an earlier October poll found 59% against the measure.</p>
      <p>While 59.5% of North Dakota voters ultimately said no to legalization, only 40.5% said yes.</p>
      <h2>Missouri</h2>
      <p>Missouri voters approved a ballot initiative to legalize medical marijuana, saying yes to  Amendment 2, but rejecting medical marijuana initiatives with varying tax rates and earmarks. The approved measure provides for a 4% tax on retail medical marijuana sales with proceeds benefitting a Missouri veteran health care fund. Voters rejected two other initiatives that looked to tax medical marijuana at 15% and 2% tax rates.</p>
      <p>While 57% of Missouri voters voted in favor of medical legalization under Amendment 2, 44% voted against. </p>
      <h2>Utah</h2>
      <p>Utah’s new law, based on Proposition 2, will permit medical use for medical card holders who have “qualifying illnesses.” It will also authorize a limited number of licensed facilities to cultivate, test and dispense marijuana, and permit medical card holders to grow up to 6 marijuana plants.</p>
      <p>While 53% of Utah voters supported medical legalization, 47% voted against.</p>
